import { handleCreatePayment, handleListOrderPayments } from "@/payments/routes/payments.route";

export async function POST(
  request: Request,
  context: { params: Promise<{ id: string }> },
): Promise<Response> {
  const { id } = await context.params;
  return handleCreatePayment(id, request);
}

export async function GET(
  request: Request,
  context: { params: Promise<{ id: string }> },
): Promise<Response> {
  const { id } = await context.params;
  return handleListOrderPayments(id, request);
}
